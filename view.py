def get_confirmation():
    confirmation = int(input('Делаем?:\n'
                         '1. Да\n'
                         '2. Нет\n'))
    if confirmation == 1:
        confirmation = True
    elif confirmation == 2:
        confirmation = False
    else:
        print('Укажите 1 или 2')
    return confirmation                                 

