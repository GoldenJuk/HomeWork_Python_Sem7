def main_menu():
    commands = ['Показать все контакты',
                'Загрузить телефонную книгу',
                'Сохранить телефонную книгу',
                'Создать новый контакт',
                'Изменить контакт',
                'Удалить контакт',
                'Найти контакт',
                'Выйти из программы']

    print(f'\nМеню телефонной книги\n----------------------')
    for i, items in enumerate(commands,1):
        print(f'{i}. {items}')    
    
    while True:
        try:
            user_input = int(input('\nВведите пункт меню:'))
            if 0 < user_input < 9:
                break
            else:
                print('\nВыбор должен быть от 1 до 8')
        except:
            print('\nОШИБКА!!! Выбор должен быть от 1 до 8')
    return user_input

